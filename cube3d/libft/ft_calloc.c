#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void *ptr;

	if (nmemb != 0 && size != 0 && size * nmemb / nmemb != size)
		return (NULL);
	if (!(ptr = (void *)malloc(nmemb * size)))
		return (NULL);
	ft_bzero(ptr, nmemb * size);
	return (ptr);
}
