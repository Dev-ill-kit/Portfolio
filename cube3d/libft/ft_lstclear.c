#include "libft.h"

void		ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list *ptr;
	t_list *temp;

	if (lst && *lst && del)
	{
		ptr = *lst;
		while (ptr)
		{
			temp = ptr;
			ptr = ptr->next;
			ft_lstdelone(temp, del);
		}
		*lst = NULL;
	}
}
