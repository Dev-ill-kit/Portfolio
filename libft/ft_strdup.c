#include "libft.h"

char		*ft_strdup(const char *s)
{
	char		*c;
	size_t		i;
	size_t		j;

	i = 0;
	j = 0;
	while (*(s + i))
	{
		i++;
	}
	if (!(c = malloc(sizeof(char) * (i + 1))))
	{
		return (0);
	}
	while (*(s + j))
	{
		*(c + j) = *(s + j);
		j++;
	}
	*(c + j) = '\0';
	return (c);
}
